SELECT 
    TRANSACTION_DATE,  
    CC_NUM,  
    MERCHANT,  
    CATEGORY,  
    AMOUNT,  
    FIRST_PURCHASE,
    LAST_PURCHASE,  
    GENDER,  
    STREET,  
    CITY,  
    STATE,  
    ZIP,  
    LAT,  
    LONG,  
    CITY_POPULATION,  
    JOB,  
    DATE_OF_BIRTH,  
    TRANSACTION_NUMBER,  
    UNIX_TIME,  
    MERCH_LAT,  
    MERCH_LONG,  
    IS_FRAUD 
FROM 
    TRANSACTIONS 
WHERE
    TRANSACTION_DATE BETWEEN '2018-12-31 00:00:00' AND '2020-06-21 23:59:59';
